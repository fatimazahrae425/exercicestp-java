package Ex8;

public class Oiseau implements Volant {
	
	 public void voler() {
	        System.out.println("L'oiseau bat des ailes pour voler");
	 }
	 
	 public void atterrir() {
	        System.out.println("L'oiseau se pose sur un arbre");
	 }

	 public void changerAltitude() {
	        System.out.println("L'oiseau monte plus haut dans le ciel ");
	 }
	 
	 public void migrer() {
	        System.out.println("L'oiseau part vers les pays chauds");
	 }

	 public void construireNid() {
	        System.out.println("L'oiseau fabrique son nid");
	 }

}
